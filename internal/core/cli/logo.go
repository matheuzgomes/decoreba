// showLogo types out the ASCII logo with an animation effect.
func showLogo() {
	for _, ch := range logoArt {
		fmt.Print(string(ch))
		time.Sleep(100 * time.Microsecond)
	}
}

// showFirstRunWelcome displays the animated logo followed by a welcome message.
func showFirstRunWelcome() {
	showLogo()
	fmt.Println()
	fmt.Println("Welcome to decoreba!")
	fmt.Println("Run 'decoreba help' to get started.")
	fmt.Println()
}

// showVersionBox prints a compact branded version line in a box.
func showVersionBox() {
	w := 42
	line := "decoreba v" + version
	pad := w - 4 - len(line)
	if pad < 1 {
		pad = 1
	}

	top := "╭" + rpt("─", w-2) + "╮"
	mid := "│  " + line + rpt(" ", pad) + "│"
	btm := "╰" + rpt("─", w-2) + "╯"

	fmt.Println(top)
	fmt.Println(mid)
	fmt.Println("│  your personal command vault" + rpt(" ", w-4-27) + "│")
	fmt.Println(btm)
}

func rpt(s string, n int) string {
	r := ""
	for i := 0; i < n; i++ {
		r += s
	}
	return r
}

// isFirstRun returns true when the config file does not exist yet.
func isFirstRun() bool {
	path, err := configPath()
	if err != nil {
		return false
	}
	_, err = os.Stat(path)
	return os.IsNotExist(err)
}

// configPath mirrors store.ConfigPath without creating directories, for the
// existence check only.
func configPath() (string, error) {
	dir, err := os.UserConfigDir()
	if err != nil {
		return "", err
	}
	return filepath.Join(dir, "decoreba", "commands.json"), nil
}
